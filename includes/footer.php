        const mainHeader = document.getElementById('mainHeader');
        const mobileMenu = document.getElementById('mobileMenu');
        const mobileMenuBtn = document.getElementById('mobileMenuBtn');
        const closeMenuBtn = document.getElementById('closeMenuBtn');
        const menuLinks = document.querySelectorAll('.menu-link');
        const stickyFooter = document.getElementById('stickyFooter');

        // Header Scroll Effect
        window.addEventListener('scroll', () => {
            if (window.scrollY > 50) {
                if (mainHeader) {
                    mainHeader.querySelector('.header-glass').classList.add('py-2');
                    mainHeader.classList.add('top-2');
                }
            } else {
                if (mainHeader) {
                    mainHeader.querySelector('.header-glass').classList.remove('py-2');
                    mainHeader.classList.remove('top-2');
                }
            }

            // Sticky Footer Visibility
            if (stickyFooter) {
                if (window.scrollY > 300) {
                    stickyFooter.classList.add('visible');
                } else {
                    stickyFooter.classList.remove('visible');
                }
            }
        });

        // Mobile Menu Toggle
        function toggleMenu() {
            if (mobileMenu) {
                mobileMenu.classList.toggle('translate-x-full');
                document.body.classList.toggle('overflow-hidden');
            }
        }

        if (mobileMenuBtn) mobileMenuBtn.addEventListener('click', toggleMenu);
        if (closeMenuBtn) closeMenuBtn.addEventListener('click', toggleMenu);
        menuLinks.forEach(link => link.addEventListener('click', toggleMenu));

        // FAQ Toggle Logic (Global)
        function toggleFaq(btn) {
            const content = btn.nextElementSibling;
            const icon = btn.querySelector('svg');
            
            if (!content || !icon) return;

            const isHidden = content.classList.contains('hidden');
            
            // Close all other FAQ items in the same section
            const section = btn.closest('section') || document;
            section.querySelectorAll('.faq-content').forEach(el => {
                el.classList.add('hidden');
                el.style.maxHeight = null;
            });
            section.querySelectorAll('svg').forEach(svg => {
                svg.classList.remove('rotate-180');
            });
            
            // Toggle current item
            if (isHidden) {
                content.classList.remove('hidden');
                icon.classList.add('rotate-180');
            }
        }

        // Modal Logic (Global)
        function openModal(id) {
            const modal = document.getElementById(id);
            if (modal) {
                modal.classList.add('active');
                document.body.style.overflow = 'hidden';
            }
        }
        function closeModal(id) {
            const modal = document.getElementById(id);
            if (modal) {
                modal.classList.remove('active');
                document.body.style.overflow = '';
            }
        }
    </script>
